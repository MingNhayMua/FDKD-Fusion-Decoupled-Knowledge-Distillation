def _detect_checkpoint_dir() -> str:
    """Auto-detect checkpoint directory based on environment."""
    # 1. Explicit override via env var
    env_dir = os.environ.get("FDKD_CHECKPOINT_DIR")
    if env_dir:
        return env_dir
    # 2. Local checkpoints directory
    if os.path.isdir(_LOCAL_CKPT):
        return _LOCAL_CKPT
    # 3. Colab default
    if os.path.isdir(_COLAB_CKPT):
        return _COLAB_CKPT
    # 4. Fallback to local (will be created if needed)
    return _LOCAL_CKPT

